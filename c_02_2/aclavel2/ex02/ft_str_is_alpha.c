int	ft_str_is_alpha(char *str)
{
	int valid;

	if (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		if ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z'))
			valid = 1;
		else
		{
			valid = 0;
			break ;
		}
		str++;
	}
	return (valid);
}
