#include "ft_doop.h"

int		ft_sum(int i, int j)
{
	return (i + j);
}

int		ft_div(int i, int j)
{
	return (i / j);
}

int		ft_sub(int i, int j)
{
	return (i - j);
}

int		ft_mult(int i, int j)
{
	return (i * j);
}

int		ft_mod(int i, int j)
{
	return (i % j);
}
